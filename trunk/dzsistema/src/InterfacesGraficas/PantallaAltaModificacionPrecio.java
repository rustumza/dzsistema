    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        //add your handling code here:
        PrecioHistorico nuevo = new PrecioHistorico();
        //Verifica campo
        if (jTextFieldPU.getText().isEmpty() == false){
            //Valida Precio Unitario
            if(Validar.controlIVA(jTextFieldPU.getText()).equals("falso")){
                JOptionPane.showMessageDialog(null, "El Precio Unitario ingresado no es válido", "¡Atención!", JOptionPane.INFORMATION_MESSAGE);
                }
            else{
                //Pone a los demas precios historios con estado false
                for(int i=0; i<producto.getPreciosHistoricos().size();i++){
                    producto.getPreciosHistoricos().get(i).setEstado(false);
                }
                nuevo.setEstado(true);
                nuevo.setPrecio(Float.parseFloat(Validar.controlIVA(jTextFieldPU.getText())));
                //Creo fecha y la formateo
                Date hoy = new Date();
                hoy = Validar.formatearFechaADate(hoy);
                nuevo.setFechaDesdeQueEntroEnVigencia(hoy);
                //Guardo Precio Historico
                PrecioHistoricoJpaController fachadaPH = new PrecioHistoricoJpaController();
                fachadaPH.create(nuevo);
                //Asigno precio al producto y lo guardo
                ProductoJpaController fachada = new ProductoJpaController();
                producto.addPrecio(nuevo);
                try {
                    fachada.edit(producto);
                } catch (NonexistentEntityException ex) {
                    Logger.getLogger(PantallaAltaModificacionPrecio.class.getName()).log(Level.SEVERE, null, ex);
                } catch (Exception ex) {
                    Logger.getLogger(PantallaAltaModificacionPrecio.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null, "Precio Guardado", "¡Atención!", JOptionPane.INFORMATION_MESSAGE);
                //Actualizo Tabla
                GUI.CargarTabla();
                this.dispose();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Ingrese un Precio Unitario", "¡Atención!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

